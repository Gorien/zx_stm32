void out_scr (uint16_t addr_pixel, uint16_t addr_attribute)
{
	attribute=memory[addr_attribute|b];
	pixel=memory[addr_pixel|b];
	if (attribute&0x80)
	{
		pixel^=flash;
		attribute&=0x7f;
	}
	DMA2_Stream1->PAR=(uint32_t)&screen_data[pixel][attribute][0];
	DMA2_Stream1->CR|=DMA_SxCR_EN;
}

static void sc_BORDER (uint8_t b)
{
	DMA2_Stream0->CR|=DMA_SxCR_EN;
}

static void sc_BRINTH (void)
{
	DMA2_Stream0->CR|=DMA_SxCR_EN;
	INT=1;
	flash_count++;
	if (flash_count==0)
	{
		flash^=0xff;
	}
}

static void sc_BRINTL (void)
{
	DMA2_Stream0->CR|=DMA_SxCR_EN;
	INT=0;
}

